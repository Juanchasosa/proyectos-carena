#Desarrollar un algoritmo para calcular la superficie de un triángulo rectángulo (base por altura dividido dos):

print("===================================================")
print("Calculo de la superficie de un triángulo rectángulo")
print("===================================================")

base = int(input("Ingrese el valor de la base: "))
altura = int(input("Ingrese el valor de la altura: "))

superficie = base * altura / 2

print("La superficie total del triángulo rectángulo es: ", superficie)
