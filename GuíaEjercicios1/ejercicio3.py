#Desarrollar un algoritmo para calcular la superficie de un cuadrado (base por altura):
print("=======================================")
print("Calculo de la superficie de un cuadrado")
print("=======================================")

base = int(input("Ingrese la base del cuadrado: "))
altura = int(input("Ingrese la altura del cuadrado: "))

superficie = base * altura

print("La superficie total del cuadrado es: ", superficie)